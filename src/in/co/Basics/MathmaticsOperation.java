package in.co.Basics;

public class MathmaticsOperation {

	public static void main(String[] args) {
		System.out.println("Mathmatics Function");
		System.out.println("Max2,5-" +Math.max(2, 5));
		System.out.println("Min2,5-" +Math.min(2, 5));
		System.out.println("Absolute3.7-"+Math.abs(3.7));
		System.out.println("Exp10-" +Math.exp(10));
		System.out.println("Random Number-" +Math.random());
		System.out.println("Square Root-" +Math.sqrt(4));
	}
	
	
	
}
