package in.co.oop;

public class Shape {
	
	private String color;
	private int Borderwidth;
	private int length;
	private int width;
	

public Shape() {
	
	this.Borderwidth=Borderwidth;
	this.color=color;
	
}
    public String getColor() {
	return color;
	
}
    public int getBorderwidth() {
     return Borderwidth;
    }
	
public int area() {
return length*width;

}
} 